<h1 id='heading'>Why e commerce is important?</h1>
<p>
	With the development of the Internet, the ability to send information is now incredibly fast. 
	Companies have realized this fact and have used the Internet as a tool to expand their businesses. 
	E-commerce, or electronic commerce, is a way to conduct minor and major business transactions online 
	instead of going into a store. It provides convenience for people who would rather be shipped an item 
	from a store instead of going to the actual store to purchase it. For major manufacturing businesses, 
	it helps them to purchase materials that they will need to make goods or provide services at sometimes 
	lower costs and without the need of having to speak to someone directly. Since the Internet is constantly 
	active, there are no closing hours when businesses or people cannot purchase from a company or person selling
	 goods on an auction site like Ebay. Thus, increasing production by eliminating wait time of ordering production 
	 materials or services and increasing profits by being able to continue selling products 24 hours a day, 7 days a week.
	 So what do you need to make an ecommerce
	 website effective?
</p>
