		<h1 id='heading'>Security – the number one issue</h1>
			<p>The reason that ecommerce websites took a while to gain precedence is that many people were worried about
			 typing in their credit card details into the Internet because of the fear of fraud. So your site has to be
			  secure. Offering an https checkout page is a must. Https notification indicates that the site is protected 
